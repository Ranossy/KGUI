--魔法属性应用时的执行函数,dwCharacterID是魔法属性作用的目标ID
function Apply(dwCharacterID, dwSkillSrcID)
	local player = GetPlayer(dwCharacterID)
	if not player then
		return
	end
	
	local playerC = GetPlayer(player.dwControllerID)
	if playerC then
		if playerC.bSprintFlag then
			RemoteCallToClient(player.dwID,"CallUIGlobalFunction","On_Freeze_End")
			return
		end
	end
	
	if not player.IsHaveBuff(12024, 1) then
		player.AddBuff(0, 99, 12024, 1, 7200)
		return
	else
		player.DelBuff(12024, 1)
	end
	RemoteCallToClient(player.dwID,"CallUIGlobalFunction","On_Freeze_End")	
end

