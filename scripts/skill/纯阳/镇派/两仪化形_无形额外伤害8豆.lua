--设置武功技能级别相关数值
function GetSkillLevelData(skill)

    local dwSkillLevel 	= skill.dwLevel;
    local nSubsection 	= 8;
    
   	----------------- 魔法属性 -------------------------------------------------
   	skill.AddAttribute(
        ATTRIBUTE_EFFECT_MODE.EFFECT_TO_SELF_AND_ROLLBACK,				-- 属性作用模式
        ATTRIBUTE_TYPE.NEUTRAL_CRITICAL_STRIKE_BASE_RATE,				-- 混元会心几率
        1000*dwSkillLevel, 							
        0			
        );		
   		
	skill.AddAttribute(
        ATTRIBUTE_EFFECT_MODE.EFFECT_TO_SELF_AND_ROLLBACK, 
        ATTRIBUTE_TYPE.MAGIC_CRITICAL_DAMAGE_POWER_BASE_KILONUM_RATE,				
	    100*dwSkillLevel,
        0
	);      	
   	
   	skill.AddAttribute(
   		ATTRIBUTE_EFFECT_MODE.EFFECT_TO_SELF_AND_ROLLBACK,				-- 属性作用模式
   		ATTRIBUTE_TYPE.SKILL_NEUTRAL_DAMAGE,							-- 魔法属性
   		tSkillData[dwSkillLevel].nDamageBase * nSubsection/10,			-- 属性值1
   		0																-- 属性值2
   		);
   		
   	skill.AddAttribute(
   		ATTRIBUTE_EFFECT_MODE.EFFECT_TO_SELF_AND_ROLLBACK,				-- 属性作用模式
   		ATTRIBUTE_TYPE.SKILL_NEUTRAL_DAMAGE_RAND,						-- 魔法属性
   		tSkillData[dwSkillLevel].nDamageRand *  nSubsection/10,							-- 属性值1
   		0																-- 属性值2
   		);
    
    skill.AddAttribute(
   		ATTRIBUTE_EFFECT_MODE.EFFECT_TO_DEST_NOT_ROLLBACK,				-- 属性作用模式
   		ATTRIBUTE_TYPE.CALL_NEUTRAL_DAMAGE,								-- 魔法属性
   		0,																-- 属性值1
   		0																-- 属性值2
