--魔法属性应用时的执行函数,dwCharacterID是魔法属性作用的目标ID
function Apply(dwCharacterID, dwSkillSrcID)
	local player = GetPlayer(dwCharacterID)
	if not player then
		return
	end

	local Add_speed = 0
	local Add_coe = 0

	--增加移动速度秘籍
	local tRecipeListSP = {1885, 1886, 1887}
	for i = 1, #tRecipeListSP do
		if player.IsSkillRecipeActive(tRecipeListSP[i], 1) then
			Add_speed = Add_speed + 1
		end
	end

	--增加减伤秘籍
	local tRecipeListCOE = {1888, 1889}
	for i = 1, #tRecipeListCOE do
		if player.IsSkillRecipeActive(tRecipeListCOE[i], 1) then
			Add_coe = Add_coe + 1
		end
	end

	----------------------------------------------------盾墙姿态附带的效果
	player.AddBuff(0, 99, 8265, 1, 40)  --免控、切换盾墙图标为收盾、盾墙状态标记
	player.AddBuff(0, 99, 8400, 1, 40) --换动作
	player.AddBuff(0, 99, 8558, 1, 40) --40秒的延长盾压释放距离
	player.AddBuff(0, 99, 8559, 1, 40) --被击回怒、被击掉格挡值
	player.AddBuff(0, 99, 8560, 1, 40) --自然掉格挡值
	if player.GetSkillLevel(13278) == 1 then
		player.AddBuff(0, 99, 8564, 1, 20) --加强版盾舞、切换盾舞图标
	end
	player.AddBuff(0, 99, 8565, 1, 40) --被击重置盾压
	player.AddBuff(0, 99, 8576, 1, 40) --盾墙无法再次盾墙
	----------------------------------------------------盾墙姿态附带的效果
	
	player.AddBuff(player.dwID, player.nLevel, 8299, 1 + Add_speed, 40)--盾墙增加移动速度

	--盾墙增加减伤
	local LvJR = player.GetSkillLevel(13363)
	if LvJR == 1 then
		player.AddBuff(player.dwID, player.nLevel, 8421, 1 + Add_coe, 40) --奇穴修饰后的强减伤
	else
		player.AddBuff(player.dwID, player.nLevel, 8300, 1 + Add_coe, 40)  --弱减伤
	end

	if player.GetSkillLevel(13370) == 1 then --防御奇穴 ，荣辉
		player.AddBuff(0, 99, 8428, 1, 40) --40秒，每秒释放一次群减伤，
	end

	if player.GetSkillLevel(13418) == 1 then --攻击奇穴，盾墙移动光环
		player.AddBuff(0, 99, 8494, 1, 40) --40秒，每秒释放一次群减伤，
	end

	--这段存buff变量的指令是执行不到的 有问题
	local BJbuff = player.GetBuff(8486, 1)
	if BJbuff then
		BJbuff.nCustomValue = nParam1
	end

	player.AddBuff(0, 99, 8486, 1, 160)  --40秒监控自己的三个BUFF

	ModityCDToUI(player, 13049, 0, 0)
end

